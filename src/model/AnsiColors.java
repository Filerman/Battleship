package model;

public class AnsiColors {
    // Reset koloru
    public static final String RESET = "\u001B[0m";

    // Kolory tekstu:
    public static final String BLUE = "\u001B[34m";   // np. woda
    public static final String GRAY = "\u001B[90m";   // np. kamień
    public static final String GREEN = "\u001B[32m";  // np. statek
    public static final String RED = "\u001B[31m";    // np. trafienie
    public static final String YELLOW = "\u001B[33m"; // np. pudło (lub inny kolor)
}
